async function delistFromStores() {
  try {
    for (const store of [
      "bf",
      "ah",
      "bsk",
      "ih",
      "pb",
      "bs",
      "hie",
      "stie",
      "hie",
      "ds",
    ]) {
        require("./config/config").config(store)
      for (let i = 0; i < delist.length; i++) {
        try {
          
        if (i  < 3581 && store === "bf") continue

        console.log(store, i, delist.length);
        const sku = delist[i];

        const product = await getProductBySku(sku);
        if (!product) {
          continue;
        }
        const productVariants = await getProductVariants(product.id);
        const totalInventory = productVariants.reduce(
          (a, c) => a + c.inventory_level,
          0
        );
        if (totalInventory > 0) {
          continue;
        }

        await deleteProduct(product.id, true);
        fs.appendFileSync(path.resolve(__dirname, "delist.log"), `on store ${store} deleted id:${product.id} ${product.name} (${sku})\n`,{encoding:"utf-8"})
        } catch(err: any) {
          if(err.status === 404) {
            i--
            continue
          }
          if(err.status === 429) {
            i--
            await new Promise(resolve => setTimeout(resolve, 5000))
            continue
          }
          throw err
        }
      }
    }
  } catch (err) {
    console.log(err);
  }
}

