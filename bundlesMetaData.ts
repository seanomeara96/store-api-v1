async function testMain() {
  try {
    // identify all products where free shipping applies
    require("./config/config").config("bf");
    const duos = (await getAllProducts({ brand_id: 154 })).filter(
      (p) => bundleSKUs[p.sku] && bundleSKUs[p.sku].length == 2 && p.inventory_level > 0
    );
    for(const duo of duos){
      console.log(duo.name)
      let totalWeight = 0;
      let totalWidth = 0; 
      let totalHeight = 0;
      let totalDepth = 0;
      const componentSKUs = bundleSKUs[duo.sku]
      for(const componentSKU of componentSKUs){
        const product = await getProductBySku(componentSKU)
         if(!product) {
          throw new Error("no product for "+ componentSKU)
         }
         totalWeight += product.weight
         totalWidth += product.width
         if(!totalHeight) totalHeight = product.height
         if(!totalDepth) totalDepth = product.depth
      }
      await updateProduct(duo.id, {
        weight: totalWeight,
        width: totalWidth,
        height: totalHeight,
        depth: totalDepth,
      })
    }
  } catch (err) {
    console.log(err);
  }
}
