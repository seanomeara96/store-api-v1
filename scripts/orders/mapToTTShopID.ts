function getDate7DaysAgo(format = "ISO") {
  const date = new Date();
  date.setDate(date.getDate() - 7);

  if (format.toUpperCase() === "RFC") {
    // RFC-2822 format
    return date.toUTCString();
  } else {
    // ISO-8601 format
    return date.toISOString();
  }
}

